def compare_strings(str1, str2):
    # 将字符串转换为小写并去除空格
    str1 = str1.lower().replace(" ", "")
    str2 = str2.lower().replace(" ", "")

    # 计算每个字符串的长度
    len_str1 = len(str1)
    len_str2 = len(str2)

    # # 如果两个字符串长度不同，返回0
    # if len_str1 != len_str2:
    #     return 0

    # 创建一个空字典来存储相同字符及其出现次数
    same_char_count = {}
    print(str1)
    print(str2)
    # 遍历第一个字符串的每个字符
    for char in str1:
        # 如果字符已经在字典中，增加其计数
        if char in same_char_count:
            same_char_count[char] += 1
        else:
            # 否则，将字符添加到字典并设置计数为1
            same_char_count[char] = 1

    # 遍历第二个字符串的每个字符
    for char in str2:
        # 如果字符已经在字典中，增加其计数
        if char in same_char_count:
            same_char_count[char] += 1
        else:
            # 否则，将字符添加到字典并设置计数为1
            same_char_count[char] = 1

    # 计算相同字符的比例
    total_chars = len_str1 + len_str2
    return sum(same_char_count.values()) / total_chars * 100

